package com.example.java.iPet;

public class DogPic {
    private int id;
    private int img;

    public DogPic(int id, int img) {

        this.id = id;
        this.img = img;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }

}
